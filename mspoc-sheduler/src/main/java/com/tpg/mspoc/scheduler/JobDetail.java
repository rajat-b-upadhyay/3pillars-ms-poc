package com.tpg.mspoc.scheduler;

public class JobDetail {
	
	int jobId = 0;
	String timestamp = null ;
	
	
	public int getJobId() {
		return jobId;
	}
	public void setJobId(int jobId) {
		this.jobId = jobId;
	}
	public String getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

		

}
